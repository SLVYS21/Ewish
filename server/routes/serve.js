async function minifyHtml(html) {
  try {
    return await minify(html, {
      collapseWhitespace: true,
      removeComments: true,
      minifyJS: true,
      minifyCSS: true,
      ignoreCustomComments: [/FB_PIXEL_ID/]
    });
  } catch (err) {
    console.error('Minification error:', err);
    return html; // Fallback to original HTML on error
  }
}


/* ── Cloudinary URL optimizer ─────────────────────────────────
   Inserts transformation params into Cloudinary upload URLs.
   Non-Cloudinary URLs pass through unchanged.
   ──────────────────────────────────────────────────────────── */
function optimizeCloudinaryUrl(url, transforms = 'f_auto,q_auto:good,w_1400,c_limit') {
  if (!url || !url.includes('res.cloudinary.com')) return url;
  // Already has transforms → don't double-add
  if (url.includes('/upload/f_') || url.includes('/upload/q_') || url.includes('/upload/w_')) return url;
  return url.replace('/upload/', `/upload/${transforms}/`);
}

router.get('/:templateName/:customName', async (req, res) => {
  // Security headers  allow embedding from app origins
  res.setHeader('Content-Security-Policy', "frame-ancestors 'self' http://localhost:3000 http://localhost:5173 https://app.mykado.store https://mykado.store");
  res.setHeader('X-Content-Type-Options', 'nosniff');

  try {
    const pub = await Publication.findOne({
      templateName: req.params.templateName,
      customName:   req.params.customName,
    }).lean();
 
    if (!pub) {
      return res.status(404).send(`
        <html><body style="font-family:sans-serif;text-align:center;padding:80px;background:#fafafa">
          <h1>404</h1><p>Aucune création trouvée à cette adresse.</p>
        </body></html>
      `);
    }

    const isPreview = req.query.preview === '1';
    const isWallFreemium = pub.templateName.startsWith('wall-of-wishes') && !pub.published;

    if (!pub.published && !isPreview && !isWallFreemium) {
      return res.status(410).send(`
        <html>
        <head>
          <meta charset="UTF-8">
          <meta name="viewport" content="width=device-width, initial-scale=1.0">
          <title>Lien non disponible</title>
          <style>
            body { margin:0; font-family:'Segoe UI',system-ui,sans-serif;
              background:#f8f8f8; display:flex; align-items:center;
              justify-content:center; min-height:100vh; }
            .card { background:#fff; border-radius:16px; padding:48px 36px;
              text-align:center; max-width:360px;
              box-shadow:0 4px 24px rgba(0,0,0,0.08); }
            .icon { font-size:3rem; margin-bottom:16px; }
            h1 { font-size:1.3rem; color:#1a1a1a; margin:0 0 10px; font-weight:700; }
            p { color:#888; font-size:0.9rem; line-height:1.6; margin:0; }
          </style>
        </head>
        <body>
          <div class="card">
            <div class="icon">🔒</div>
            <h1>Ce lien n'est plus disponible</h1>
            <p>Cette création a été dépubliée par son auteur.</p>
          </div>
        </body>
        </html>
      `);
    }
 
    const TMPL_DIR = process.env.TEMPLATES_DIR ||
      (fs.existsSync(path.join(__dirname, '../templates'))
        ? path.join(__dirname, '../templates')
        : path.join(__dirname, '../../templates'));
    const templatePath = path.join(TMPL_DIR, `${pub.templateName}/index.html`);
    if (!fs.existsSync(templatePath)) {
      return res.status(404).send('<h1>Template introuvable</h1>');
    }
 
    let html = fs.readFileSync(templatePath, 'utf8');

    // Load custom fonts from DB and inject @font-face declarations
    let fontFaceCSS = '';
    try {
      const customFonts = await Font.find({}).lean();
      if (customFonts.length) {
        fontFaceCSS = customFonts.map(f =>
          `@font-face { font-family: '${f.name}'; src: url('${f.url}') format('${f.format}'); font-display: swap; }`
        ).join('\n');
      }
    } catch {}

    /* ── BubbleFont per-letter Cloudinary CDN loading ───────────
       For each unique uppercase letter in the recipient's name
       (+ H, B, D always needed for "HBD" balloon letters), inject
       one @font-face rule with unicode-range pointing to the
       per-letter WOFF2 on Cloudinary CDN.  The browser downloads
       only the letter files it actually needs to render.
       Falls back to the local WOFF2 subset if CDN URLs aren't
       available yet (run uploadBubbleLetters.js to populate them).
       ────────────────────────────────────────────────────────── */
    let bubbleFontPreloads = '';
    let bubbleFontFaces    = '';
    if (BUBBLE_TEMPLATES.has(pub.templateName) && Object.keys(BUBBLE_LETTER_URLS).length > 0) {
      const nameText    = String((pub.data && pub.data.name) || '');
      const neededChars = [...new Set(('HBD' + nameText).toUpperCase().split('').filter(c => /[A-Z]/.test(c)))].sort();

      const preloadTags = [];
      const fontFaceRules = [];

      for (const letter of neededChars) {
        const url = BUBBLE_LETTER_URLS[letter];
        if (!url) continue;
        const codePoint = `U+${letter.charCodeAt(0).toString(16).padStart(4, '0').toUpperCase()}`;
        preloadTags.push(`<link rel="preload" href="${url}" as="font" type="font/woff2" crossorigin>`);
        fontFaceRules.push(
          `@font-face{font-family:'BubbleFont';src:url('${url}')format('woff2');unicode-range:${codePoint};font-display:block;}`
        );
      }

      bubbleFontPreloads = preloadTags.join('\n  ');
      bubbleFontFaces    = fontFaceRules.join('\n');
    }

    const s     = pub.style || {};
    const scale = s.fontSize === 'small' ? '0.85' : s.fontSize === 'large' ? '1.15' : '1';
    const bgs   = s.backgrounds || {};

    /* ── CSS: section background variables ─────────────────── */
    const bgCssLines = [];
    Object.entries(bgs).forEach(([key, bg]) => {
      if (!bg || !bg.value) return;
      let bgValue;
      if      (bg.type === 'color')    bgValue = bg.value;
      else if (bg.type === 'gradient') bgValue = bg.value;
      else if (bg.type === 'image')    bgValue = `url("${optimizeCloudinaryUrl(bg.value)}") center/cover no-repeat`;
      if (bgValue) {
        bgCssLines.push(`  --bg-${key}: ${bgValue};`);
        if (bg.overlay != null) bgCssLines.push(`  --bg-${key}-overlay: ${bg.overlay};`);
        if (bg.blur    != null) bgCssLines.push(`  --bg-${key}-blur: ${bg.blur}px;`);
      }
    });

    // Inject Google Fonts link for the chosen font family
    const chosenFont   = s.fontFamily || 'Work Sans';
    const gFontQuery   = GOOGLE_FONTS_MAP[chosenFont];
    const googleFontLink = gFontQuery
      ? `<link rel="preconnect" href="https://fonts.googleapis.com"><link rel="preconnect" href="https://fonts.gstatic.com" crossorigin><link href="https://fonts.googleapis.com/css2?family=${gFontQuery}&display=swap" rel="stylesheet">`
      : '';
    if (googleFontLink) {
      html = html.replace('</head>', `${googleFontLink}\n</head>`);
    }

    // Inject per-letter preload links early in <head>
    if (bubbleFontPreloads) {
      html = html.replace('<head>', `<head>\n  ${bubbleFontPreloads}`);
    }

    const isWallTemplate = pub.templateName.startsWith('wall-of-wishes');

    /* Resolve wall colors with smart fallbacks:
       - explicit user customization wins
       - legacy schema defaults (#ff69b4/#ffb347) are ignored as if missing
       - falls back to WALL_DEFAULTS[templateName] (template's design colors) */
    let wallPrimary, wallAccent;
    if (isWallTemplate) {
      const tplDef = WALL_DEFAULTS[pub.templateName] || WALL_DEFAULTS['wall-of-wishes'];
      const userPrimary = (s.primaryColor && s.primaryColor !== LEGACY_SCHEMA_DEFAULTS.primaryColor) ? s.primaryColor : null;
      const userAccent  = (s.accentColor  && s.accentColor  !== LEGACY_SCHEMA_DEFAULTS.accentColor)  ? s.accentColor  : null;
      wallPrimary = userPrimary || tplDef.primaryColor;
      wallAccent  = userAccent  || tplDef.accentColor;
    } else {
      wallPrimary = s.primaryColor || '#ff69b4';
      wallAccent  = s.accentColor  || '#ffb347';
    }

    /* Admin detection  show admin banner in wall template when admin views it */
    let isAdmin = false;
    let adminReturnUrl = '';
    if (isWallTemplate) {
      try {
        const token = req.cookies?.ww_admin_token;
        if (token) {
          const decoded = require('jsonwebtoken').verify(token, process.env.JWT_SECRET);
          if (decoded) {
            isAdmin = true;
            const appOrigin = process.env.APP_URL || (process.env.NODE_ENV !== 'production' ? 'http://localhost:3000' : 'https://app.mykado.store');
            adminReturnUrl = `${appOrigin}/ewish-admin/wall/${String(pub._id)}`;
          }
        }
      } catch {}
    }

    const injection = `
<style>
${bubbleFontFaces ? bubbleFontFaces + '\n' : ''}${fontFaceCSS ? fontFaceCSS + '\n' : ''}  :root {
    --primary:    ${wallPrimary};
    --accent:     ${wallAccent};
    --gold:       ${wallPrimary};
    --rose:       ${wallAccent};
    --font:       '${s.fontFamily   || 'Work Sans'}', sans-serif;
    --font-b:     '${s.fontFamily   || 'Work Sans'}', sans-serif;
    --fs-scale:   ${scale};
    --text-color: ${s.textColor     || '#333333'};
    --text-muted: ${s.textMuted     || '#888888'};
${bgCssLines.join('\n')}
  }
  body { font-family: var(--font) !important; }
</style>
<script>
  /* ── Publication data injected by server ── */
  const _rawData = ${JSON.stringify(pub.data || {})};
  const _jarCfg  = ${JSON.stringify(pub.jarConfig || null)};

  window.__WW_DATA__  = {
    ...(_jarCfg ? { ..._rawData, jarConfig: _jarCfg } : _rawData),
    publicationId: '${String(pub._id)}',
    apiBase: '${process.env.API_BASE_URL || ''}',
    wishesEnabled: ${pub.cagnotteConfig?.wishesEnabled !== false},
    isPrivate: ${pub.cagnotteConfig?.isPrivate || (isWallTemplate && (pub.data?.isPrivate || false))},
    accessCode: '${((pub.cagnotteConfig?.accessCode || (isWallTemplate ? pub.data?.accessCode : '')) || '').replace(/'/g, "\\'")}',
    requireModeration: ${pub.cagnotteConfig?.requireModeration || false},
    isPaid: ${pub.isPaid || false},
    minContribution: ${pub.cagnotteConfig?.minContribution || 0},
    maxContribution: ${pub.cagnotteConfig?.maxContribution || 0},
    isAdmin: ${isAdmin},
    adminReturnUrl: '${adminReturnUrl}',
    cagnotte: ${pub.cagnotteConfig?.enabled ? JSON.stringify({
      enabled: true,
      name: pub.cagnotteConfig.description || '',
      goal: pub.cagnotteConfig.goal || 0,
      emoji: '🎁',
      collected: 0,
      contributors: 0,
      minAmount: pub.cagnotteConfig.minContribution || 0,
      maxAmount: pub.cagnotteConfig.maxContribution || 0,
    }) : 'null'},
  };
  window.__WW_STYLE__ = ${JSON.stringify(pub.style || {})};
  window.__WW_META__  = ${JSON.stringify({
    id:           String(pub._id),
    title:        pub.title,
    templateName: pub.templateName,
    customName:   pub.customName,
  })};
 
  /* ── Decorations (read by ww-engine.js) ── */
  window.__WW_API_BASE__  = '${process.env.API_BASE_URL || ''}';
  window.__WW_BRANDING__ = ${JSON.stringify({
    show: Boolean(pub.showBranding),
    url: pub.brandingUrl || process.env.BRANDING_URL || 'https://wa.me/+2290159571057',
    label: pub.brandingText || 'Crée le tien sur myKado ✨'
  })};
  window.__WW_DECO__     = ${JSON.stringify(pub.decorations || [])};
  window.__WW_WIDGETS__ = ${JSON.stringify(pub.widgets || [])};
  window.__WW_PHOTO_TRANSFORMS__ = ${JSON.stringify(pub.photoTransforms || {})};
  window.__WW_CONFETTI_TYPE__ = '${(pub.style && pub.style.confettiType) || 'default'}';
<\/script>`;
  // window.__WW_BRANDING__  = ${JSON.stringify({
  //   show: ${pub.showBranding ? 'true' : 'false'},
  //   url:  '${(pub.brandingUrl || process.env.BRANDING_URL || 'https://wa.me/your_number').replace(/'/g, "\\'")}',
  //   label: 'Crée le tien sur eWishWell ✨',
  // })};
  
    /* ── Inject engine script tag if not already present ───── */
    if (!html.includes('ww-engine.js')) {
      html = html.replace('</head>', '<script src="/templates/shared/ww-engine.js"><\/script>\n</head>');
    }
 
    html = html.replace('</head>', injection + '\n</head>');

    // ── Incrémenter le compteur de visites ──────────────────
    Publication.findByIdAndUpdate(pub._id, {
      $inc: { views: 1 },
      lastViewedAt: new Date(),
    }).catch(() => {});

    if (!isPreview) {
      Publication.findByIdAndUpdate(pub._id, {
        $inc: { views: 1 },
        lastViewedAt: new Date(),
      }).catch(() => {});
    }

    html = await minifyHtml(html);

    res.setHeader('Content-Type', 'text/html; charset=utf-8');
    res.send(html);
  } catch (e) {
    console.error(e);
    res.status(500).send('<h1>Erreur serveur</h1><pre>' + e.message + '</pre>');
  }
});

module.exports = router;